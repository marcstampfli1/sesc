pub const ACCESSORIES: &[(u32, &str)] = GOODS;

/// Sekiro item-category high-nibble values.
pub mod category {
    pub const WEAPON: u16 = 0x0000;
    pub const PROTECTOR: u16 = 0x1000;
    pub const ACCESSORY: u16 = 0x2000;
    pub const GOODS: u16 = 0x4000;
}

/// Resolve `(category, id)` to an item name.  Returns `None` if the
/// id is not in the known dictionary.
pub fn name_of(category: u16, id: u32) -> Option<&'static str> {
    let table: &[(u32, &str)] = match category {
        category::WEAPON => WEAPONS,
        category::PROTECTOR => PROTECTORS,
        category::ACCESSORY => ACCESSORIES,
        category::GOODS => GOODS,
        _ => return None,
    };
    table
        .binary_search_by_key(&id, |(i, _)| *i)
        .ok()
        .map(|i| table[i].1)
}

/// Category-name helper.
pub fn category_name(category: u16) -> &'static str {
    match category {
        category::WEAPON => "Weapon",
        category::PROTECTOR => "Protector",
        category::ACCESSORY => "Accessory",
        category::GOODS => "Goods",
        _ => "Unknown",
    }
}
